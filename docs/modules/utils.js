/**
 * Pure DOM-escaping utility — prevents XSS when inserting
 * user-controlled or external strings into innerHTML.
 */
export function escapeHtml(str) {
  return String(str)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#039;");
}
